class Solution
{
    public:
    Node* addOne(Node *head) 
    {
        stack<Node*>stk;
        Node* cur=head;
        while(cur!=NULL){
            stk.push(cur);
            cur=cur->next;
        }
        while(!stk.empty() && stk.top()->data==9){
            stk.top()->data=0;
            stk.pop();
        }
        if(stk.size())stk.top()->data=stk.top()->data+1;
        else{
            Node* node=new Node(1);
            node->next=head;
            head=node;
        }
        // cur->data=cur->data+1;
        return head;
    }
};

